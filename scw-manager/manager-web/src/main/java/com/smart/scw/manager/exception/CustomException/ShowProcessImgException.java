package com.smart.scw.manager.exception.CustomException;

public class ShowProcessImgException extends Exception {

    public ShowProcessImgException() {
    }

    public ShowProcessImgException(String message) {
        super(message);
    }

    public ShowProcessImgException(String message, Throwable cause) {
        super(message, cause);
    }

    public ShowProcessImgException(Throwable cause) {
        super(cause);
    }

    public ShowProcessImgException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
